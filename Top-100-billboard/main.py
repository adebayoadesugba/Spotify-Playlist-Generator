import requests
from bs4 import BeautifulSoup
import spotipy
from spotipy.oauth2 import SpotifyOAuth

# 🚨 SECURITY REMINDER: Reset your Client Secret on the Spotify Dashboard when you are done!
YOUR_APP_CLIENT_ID = ""
YOUR_APP_CLIENT_SECRET = ""
REDIRECT_URI = "https://developer.spotify.com/dashboard"

# 1. Authenticate with the required scope
sp = spotipy.Spotify(
    auth_manager=SpotifyOAuth(
        scope="playlist-modify-private",
        redirect_uri=REDIRECT_URI,
        client_id=YOUR_APP_CLIENT_ID,
        client_secret=YOUR_APP_CLIENT_SECRET,
        show_dialog=True,
        cache_path="token.txt"
    )
)

# Fetch ID dynamically to prevent 403 errors!
userID = sp.current_user()["id"]
print(f"Logged in as: {userID}")

date = "2026-01-10" # Changed to a past date so Billboard actually has data!

# ---------------------------------------------------------
# COMMENTED OUT SO IT STOPS MAKING EMPTY PLAYLISTS.
# IF YOU WANT A NEW PLAYLIST, UNCOMMENT THIS BLOCK.
# ---------------------------------------------------------
